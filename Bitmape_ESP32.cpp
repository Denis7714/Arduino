void setup(){ 

  vspi = new SPIClass(VSPI);  
  
  Serial.begin(115200);
  Serial.println("Graphical interface ver 1.0 - 09.12.2021.");

  vspi->begin();
  pinMode(SS, OUTPUT);
  pinMode(GPIO_33, OUTPUT);
  pinMode(GPIO_35, INPUT);
  digitalWrite(GPIO_33, LOW);
  digitalWrite(SS, HIGH);
}

void loop(){ 

  vspi->beginTransaction(SPISettings(spiClk, MSBFIRST, SPI_MODE0));
  while(GPIO_35 == LOW);
  digitalWrite(GPIO_33, HIGH);
  digitalWrite(SS, LOW);
  
  vspi->write(0x02);
  vspi->write(0x00);
  vspi->write(0x00);
  
  vspi->writeBytes(Pac_MAN, 1024);


  Serial.println("Postavljanje prve slike");

  digitalWrite(SS, HIGH);
  vspi->endTransaction();
  digitalWrite(GPIO_33, LOW);                 
  
  delay(3000);

  vspi->beginTransaction(SPISettings(spiClk, MSBFIRST, SPI_MODE0));
  while(GPIO_35 == LOW);
  digitalWrite(GPIO_33, HIGH);
  digitalWrite(SS, LOW);

  vspi->write(0x02);
  vspi->write(0x00);
  vspi->write(0x00);
  
  vspi->writeBytes(Slika, 1024);
 
  Serial.println("Postavljanje druge slike");

  digitalWrite(SS, HIGH);
  vspi->endTransaction();
  digitalWrite(GPIO_33, LOW);
  
  delay(3000);
}